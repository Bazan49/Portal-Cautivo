    echo "‚ùå Error creando interfaz virtual $AP_INTERFACE"
    echo "üí° Verifica que la interfaz $WIFI_INTERFACE soporte modo AP"
    echo "   Ejecuta: iw list | grep -A 10 'Supported interface modes'"
    exit 1
