    echo "üì° Interfaces disponibles:"
    ip link show | grep -E "^[0-9]+:" | awk -F: '{print "   - " $2}' | tr -d ' '
    echo ""
    echo "üí° Soluci√≥n: Ejecuta sudo ./setup_portal.sh para reconfigurar"
    exit 1
fi

# Verificar que la interfaz WiFi existe
if ! ip link show "$WIFI_INTERFACE" > /dev/null 2>&1; then
    echo "‚ùå ERROR: La interfaz WiFi '$WIFI_INTERFACE' no existe"
    echo "üì° Interfaces disponibles:"
    ip link show | grep -E "^[0-9]+:" | awk -F: '{print "   - " $2}' | tr -d ' '
    echo ""
    echo "üí° Soluci√≥n: Ejecuta sudo ./setup_portal.sh para reconfigurar"
    exit 1
fi

# Verificar que la interfaz de Internet est√° UP
if ! ip link show "$INTERNET_INTERFACE" | grep -q "state UP"; then
    echo "‚ö†Ô∏è  ADVERTENCIA: La interfaz $INTERNET_INTERFACE no est√° activa"
    echo "   Intentando activarla..."
    ip link set dev "$INTERNET_INTERFACE" up
    sleep 2
    
    if ! ip link show "$INTERNET_INTERFACE" | grep -q "state UP"; then
        echo "‚ùå No se pudo activar $INTERNET_INTERFACE"
        echo "üí° Verifica la conexi√≥n f√≠sica (cable/WiFi)"
